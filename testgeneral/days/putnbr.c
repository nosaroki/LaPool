#include <stdio.h>
#include <unistd.h>

char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (src[i] != '\0' && i < n)
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}


int	main(void)
{
	char	dest[] = "Hello There";
	char	src[] = "General Kenobi";

	ft_strncpy(dest, src, 4);
	printf("%s", dest);
	return (0);
}
